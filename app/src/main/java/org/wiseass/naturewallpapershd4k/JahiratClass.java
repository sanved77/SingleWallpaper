public class JahiratClass {

    String name, url, image;

    public JahiratClass(String name, String image, String link){
        this.name = name;
        this.url = link;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }

    public String getUrl() {
        return url;
    }
}
